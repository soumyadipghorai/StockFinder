quick_summary = """
**Price-to-Earnings Ratio (PE)**

`PE = Market Price per Share/Earnings per Share (EPS)`

where, `EPS=Number of Outstanding Shares/Net Profit`

Indicates how much investors are willing to pay for each unit of earnings. A higher PE suggests higher growth expectations, but can also indicate overvaluation.

---
**Book Value per Share (BV)**

`BV = (Total Assets - Total Liabilities)/Number of Outstanding Shares`

Represents the net asset value of the company per share. Often compared to the market price to evaluate undervaluation or overvaluation.

---
**Return on Capital Employed (ROCE)**

`ROCE = (Earnings Before Interest and Taxes (EBIT)/Capital Employed)x100`

Where:  
Capital Employed = Total Assets - Current Liabilities

Measures how efficiently a company is using its capital to generate profits. Higher ROCE indicates better capital efficiency.

---
**Return on Equity (ROE)**

`ROE = Net Income/Shareholders' Equity`

Shows the profitability relative to the equity held by shareholders. A higher ROE suggests the company is effective at generating returns on invested capital.
"""